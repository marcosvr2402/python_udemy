# Tipo de dado BOOL (boolean)
#Ao quyestionar algum programa, só existem duas respostas possível:
#Sim ou não (True ou false)
#Existem vários operadores para questionar
#Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro
print(10 == 10)
print(10==1)
print(type(10==10))
print(type(10==1)) #se for true ou false não muda nada para o tipo de classe

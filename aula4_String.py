'''
Python = linguagem de programação, toda linguagem pode ser dinamica, fraca, forte ou estática
Tipo de tipagem = dinâmica o python ja sabe qual o tipo da informação passada pra ele / forte
str -> string -> texto
Strings são textos que estão dentro de aspas
Tipagem estática tem que identificar o que é
exemplo: print(1234), no python sabe que é numero, em outras linguas não
'''
#aspas simples ou aspas duplas o python reconhece como string
print(1234)
print('Marcos Rodrigues')
print("Marcos Rodrigues")

#caracter de ESCAPE é \, significa que ele pula o próximo caracter, ele não interpreta
#para colocar aspas duplas, basta inverter, por exemplo, aspas duplas dentro de aspas simples
print("Marcos 'Vinicius'")

#o R é um caracter de ESCAPE mas que mostra
print(r"marcos \"Vinicius\"")
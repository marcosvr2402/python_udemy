adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 5
print('Multplicação', multiplicacao)

divisao = 10 / 2.2 #sempre retorna float (ponto flutuante) nesse
print('Divisão', divisao)

divisao_inteira = 10 // 2.5 #ela resume o número, arredonda
print('Divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

modulo = 55 % 2 #resto da divisão, aquele resultado embaixo da divisão
print('Módulo', modulo)

#modulo serve para saber se um numero é divisivel por aquele, logo se sobra zero é multiplo/divisivel
